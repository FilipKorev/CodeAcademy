let celsius = prompt("What is the temperature in celsius?");
let fahrenheit = 32 + (celsius * 1.8);
alert(celsius + "C is " + fahrenheit + "F.");

let fahrenheit1 = prompt("What  is the temperature in fahrenheit?");
let celsius1 = Math.floor((fahrenheit1 - 32) * 0.5556);
alert(fahrenheit1 + "F is " + celsius1 + "C.");
